     public void setValues()
     {
         try{
                Class.forName("org.sqlite.JDBC");
		Connection conn = DriverManager.getConnection("jdbc:sqlite:C:\\eclipse\\test.db");
		Statement stmt = conn.createStatement();
                
                DateFormat dateFormat = new SimpleDateFormat("YYYY-MM-dd");
                java.util.Date d = new java.util.Date();
                String yourDate = dateFormat.format(d);
        
                ResultSet rset = stmt.executeQuery("SELECT name , doctor , status FROM Appointment where doctor = '" + doctor + "' and status = 'Yes' and date = '" + yourDate + "';");
                int i = 0;
                while(rset.next())
                {
                    
                    String name = rset.getString("name");
                    String doctor = rset.getString("doctor");
                  
                    String status = rset.getString("status");
                    
                    
                    DefaultTableModel defaultModel = (DefaultTableModel) jTable1.getModel();;
                    defaultModel.addRow(new Object[]{"" , "" , "" , "" , ""});
                    jTable1.setValueAt(name , i , 0);
                    jTable1.setValueAt(doctor , i , 1);
                    jTable1.setValueAt(status , i , 2);
                    i++;
                    
                }
               
                 conn.close();
                 stmt.close();
            }
            catch(Throwable e)
            {
                System.out.println(e.getMessage());
            }
    }  
