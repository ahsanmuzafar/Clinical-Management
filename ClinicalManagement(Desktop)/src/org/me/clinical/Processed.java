    public void showValues()
    {
        try{
                Class.forName("org.sqlite.JDBC");
		Connection conn = DriverManager.getConnection("jdbc:sqlite:C:\\eclipse\\test.db");
		Statement stmt = conn.createStatement();
                
                
                ResultSet rset = stmt.executeQuery("SELECT name , contact  FROM Patient where id = " + pid + " and doctor = '" + dname + "';");
                
                if(rset.next())
                {
                    
                    String name = rset.getString("name");
                   
                    contact = rset.getString("contact");
                   
                    PRName.setText(name);
                    
                    Prid.setText(Integer.toString(pid));
                    
                    CrNo.setText(contact);
                    
                    conn.close();
                    stmt.close();
                    
                    
                }
                else
                {
                    JOptionPane.showMessageDialog(null , "No record found");
                    conn.close();
                    stmt.close();
                    this.dispose();
                    new DProfile().setVisible(true);
                    
                    
                }
               
                 
            }
            catch(Throwable e)
            {
                System.out.println(e.getMessage());
            }
    }  
