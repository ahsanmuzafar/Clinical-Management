    private void CompleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CompleteActionPerformed

        try{
                Class.forName("org.sqlite.JDBC");
		Connection conn = DriverManager.getConnection("jdbc:sqlite:C:\\eclipse\\test.db");
		Statement stmt = conn.createStatement();
                
                String s = DField.getText();
                DateFormat dateFormat = new SimpleDateFormat("dd-MM-YYYY");
                Date d = new Date();
                String yourDate = dateFormat.format(d);
                
                stmt.executeUpdate("Update Patient SET disease = '" + s + "',lastvisit = '" + yourDate + "' where id = " + pid + ";");
                stmt.executeUpdate("Update Appointment SET status = 'Yes' where contact = '" + contact + "';");
                JOptionPane.showMessageDialog(null , "Patient record has been successfully updated");
                
               
                 conn.close();
                 stmt.close();
            }
            catch(Throwable e)
            {
                System.out.println(e.getMessage());
            }
        
        this.dispose();
        new DProfile().setVisible(true);
    }//GEN-LAST:event_CompleteActionPerformed

